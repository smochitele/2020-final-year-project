package com.example.smartsecuredhome.ui.slideshow;

class CaseStringDate {

    public int CaseID ;
    public String date ;
    public String time ;
    public String ResolutionDate;
    public String ResolutionResolutionTime;
    public String Description ;
    public String UserID ;
    public int HouseID ;
    public String RespondentID ;

    public CaseStringDate() {
    }

    public int getCaseID() {
        return CaseID;
    }

    public void setCaseID(int caseID) {
        CaseID = caseID;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getResolutionDate() {
        return ResolutionDate;
    }

    public void setResolutionDate(String resolutionDate) {
        ResolutionDate = resolutionDate;
    }

    public String getResolutionResolutionTime() {
        return ResolutionResolutionTime;
    }

    public void setResolutionResolutionTime(String resolutionResolutionTime) {
        ResolutionResolutionTime = resolutionResolutionTime;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getUserID() {
        return UserID;
    }

    public void setUserID(String userID) {
        UserID = userID;
    }

    public int getHouseID() {
        return HouseID;
    }

    public void setHouseID(int houseID) {
        HouseID = houseID;
    }

    public String getRespondentID() {
        return RespondentID;
    }

    public void setRespondentID(String respondentID) {
        RespondentID = respondentID;
    }
}
